# --- Ранги (визуальные пороги) ----------------------------------------------
@dataclass(frozen=True)
class Rank:
    """Визуальный ранг по диапазону MMR."""

    min_mmr: int  # нижняя граница (включительно)
    emoji: str
    name: str


# Пороги и названия рангов. Можно менять без миграций: достаточно
# отредактировать список (он должен быть отсортирован по min_mmr).
#
# Ранги = ПУТЬ игрока по миру Возни (влияние, котость), НЕ деньги и НЕ репутация.
# Прогрессия: залётный → города мира → котость → миф. Пороги НЕ трогаем (баланс).
# ВАЖНО: список продублирован на сайте в v0-voznya/lib/queries.ts (MMR_RANKS) —
# менять оба места синхронно.
RANKS: tuple[Rank, ...] = (
    Rank(0, "🥉", "Залётный"),
    Rank(1000, "🥈", "Бродяга Утрехта"),
    Rank(2500, "🥇", "Свой в Зволле"),
    Rank(5000, "💎", "Котейший"),
    Rank(10000, "👑", "Архидрун"),
    Rank(25000, "🔥", "Боженька Возни"),
)



def get_rank(mmr: int) -> Rank:
    """Возвращает ранг для указанного значения MMR."""
    current = RANKS[0]
    for rank in RANKS:
        if mmr >= rank.min_mmr:
            current = rank
        else:
            break
    return current


