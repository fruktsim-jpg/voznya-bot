"""Настройки системы рейтинга MMR — награды, ранги и тексты.

==============================================================================
ЭТОТ ФАЙЛ МОЖНО РЕДАКТИРОВАТЬ, не зная программирования.

* Чтобы изменить, сколько MMR даёт действие — поменяй число в MMR_AWARDS.
* Чтобы изменить пороги/названия рангов — правь RANKS (БЕЗ миграций).
* Тексты команд — внизу файла.

MMR — это единый игровой рейтинг (общий прогресс). Игрок видит ТОЛЬКО «MMR»,
слово XP в интерфейсе не используется. Сообщения MMR НЕ дают — рейтинг растёт
только за игровые действия. MMR нельзя купить, передать или обменять.
==============================================================================
"""

from __future__ import annotations

from dataclasses import dataclass

# --- Категории источников (значение колонки mmr_entries.source) -------------
SOURCE_TREASURE = "treasure"
SOURCE_DUEL = "duel"
SOURCE_FARM = "farm"
SOURCE_ACHIEVEMENT = "achievement"
SOURCE_EVENT = "event"
SOURCE_ADMIN = "admin"


# --- Награды MMR за игровые действия ----------------------------------------
# Все значения настраиваемы. Сообщения здесь намеренно отсутствуют —
# активность (сообщения) не даёт рейтинг.
MMR_TREASURE = 50          # найден клад
MMR_DUEL_WIN = 100         # победа в дуэли
MMR_DUEL_PARTICIPATION = 25  # участие в дуэли (обоим игрокам)
MMR_FARM = 10              # успешная ферма

# Ачивки по «редкости». Редкость выводится из каталога достижений:
# легендарные — категория "legend"; редкие — секретные (hidden=True);
# остальные — обычные. См. mmr_for_achievement().
MMR_ACHIEVEMENT = 100      # обычная ачивка
MMR_ACHIEVEMENT_RARE = 250  # редкая (секретная) ачивка
MMR_ACHIEVEMENT_LEGENDARY = 1000  # легендарная ачивка


# --- Ранги (визуальные пороги) ----------------------------------------------
@dataclass(frozen=True)
class Rank:
    """Визуальный ранг по диапазону MMR."""

    min_mmr: int  # нижняя граница (включительно)
    emoji: str
    name: str


# Пороги и названия рангов. Можно менять без миграций: достаточно
# отредактировать список (он должен быть отсортирован по min_mmr).
RANKS: tuple[Rank, ...] = (
    Rank(0, "🥉", "Новичок"),
    Rank(1000, "🥈", "Бродяга"),
    Rank(2500, "🥇", "Авторитет"),
    Rank(5000, "💎", "Легенда"),
    Rank(10000, "👑", "Элита"),
    Rank(25000, "🔥", "Боженька Возни"),
)


def get_rank(mmr: int) -> Rank:
    """Возвращает ранг для указанного значения MMR."""
    current = RANKS[0]
    for rank in RANKS:
        if mmr >= rank.min_mmr:
            current = rank
        else:
            break
    return current


# Сколько игроков показывать в «топммр».
