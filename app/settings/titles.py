"""Система титулов Возни — ранги на основе ВСЕГО ЗАРАБОТАННОГО (total_earned).

Титул отражает совокупный продуктивный заработок игрока за всё время
(ферма, клады, достижения, номинации), а НЕ текущий баланс. Поэтому:
* прогрессия монотонна — титул нельзя потерять, проиграв ешки в казино;
* пороги предсказуемо считаются из дохода фермы/кладов (см. ECONOMY.md);
* азартные игры не «накручивают» ранг.

Текущий баланс (богатство) показывается отдельно как место в рейтинге (/топ).

==============================================================================
ЭТУ ТАБЛИЦУ МОЖНО РЕДАКТИРОВАТЬ, не зная программирования.
Каждая строка — это (порог по «всего заработано», эмодзи, название титула).
Пороги рассчитаны под доход ~45 ешек/день активной игры (см. ECONOMY.md):
новый титул примерно каждые 3–7 дней в начале и реже в эндгейме (престиж).
==============================================================================
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Title:
    """Один титул (ранг)."""

    min_earned: int
    emoji: str
    name: str

    @property
    def label(self) -> str:
        """Эмодзи + название, например «💊 Аптекарь»."""
        return f"{self.emoji} {self.name}"


# Список титулов по возрастанию порога «всего заработано».
# Ожидаемые сроки (при ~45 ешек/день активной игры) — в комментариях.
TITLES: list[Title] = [
    Title(0, "🌱", "Щавель"),              # старт
    Title(100, "🍑", "Персик"),            # ~день 3
    Title(250, "🥔", "Картофель"),         # ~день 7
    Title(500, "🐀", "Гой"),               # ~день 14
    Title(800, "🍺", "Бурмалда"),          # ~день 21
    Title(1200, "💊", "Аптекарь"),         # ~день 30
    Title(2000, "🎰", "Лудик"),            # ~день 45
    Title(3000, "⚔️", "Возняк"),           # ~день 65
    Title(4500, "🏆", "Авторитет Возни"),  # ~день 90
    Title(7000, "👑", "Король Возни"),     # ~день 130
    Title(12000, "☢️", "Меллстрой"),       # ~день 210 (эндгейм-престиж)
]


def get_title(earned: int) -> Title:
    """Возвращает текущий титул по сумме «всего заработано»."""
    current = TITLES[0]
    for title in TITLES:
        if earned >= title.min_earned:
            current = title
        else:
            break
    return current


def get_next_title(earned: int) -> Title | None:
    """Возвращает следующий титул (или None, если достигнут максимум)."""
    for title in TITLES:
        if title.min_earned > earned:
            return title
    return None
