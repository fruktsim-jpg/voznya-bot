from __future__ import annotations

# --- Варны (предупреждения) -------------------------------------------------

# Сколько активных варнов нужно, чтобы человека автоматически замьютило.
WARN_MUTE_THRESHOLD = 3

# На сколько секунд авто-мьют при достижении порога варнов (по умолчанию сутки).
WARN_MUTE_SECONDS = 24 * 60 * 60

# Сколько живёт один варн, прежде чем «протухнет» (перестанет считаться).
# 0 = варны не протухают (живут, пока их не снимут вручную).
WARN_TTL_SECONDS = 30 * 24 * 60 * 60  # 30 дней


# --- Длительности (для парсинга 10m / 2h / 1d / 0=навсегда) ------------------

# Дефолтная длительность мьюта, если её не указали в команде (секунды).
DEFAULT_MUTE_SECONDS = 60 * 60  # 1 час

# Максимальная длительность ограничения через restrictChatMember (секунды).
# Telegram трактует срок > ~366 дней или < 30 сек как «навсегда».
MAX_RESTRICT_SECONDS = 366 * 24 * 60 * 60

# Допустимые суффиксы длительности: s/m/h/d/w (+ русские с/м/ч/д/н).
