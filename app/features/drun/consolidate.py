"""Консолидация памяти: новые факты ВЫТЕСНЯЮТ устаревшие, а не копятся вечно.

Проблема накопительной памяти: «живёт в Москве» и позже «переехал в Питер»
оба оставались в self_facts, и друн путался. Здесь — дешёвая (без LLM)
эвристика: у факта определяем ТЕМУ (город, возраст, работа, учёба, отношения,
питомец...). Если приходит свежий факт по той же теме про того же человека —
он ЗАМЕЩАЕТ старый, а не дублирует. Факты без узнаваемой темы просто
накапливаются с дедупом.

Чистые функции, тестируются без БД.
"""

from __future__ import annotations

import re

# Тема факта → набор маркеров (подстроки в нижнем регистре). Порядок важен:
# первая совпавшая тема и выигрывает. Темы подобраны под «факты о себе».
_TOPIC_MARKERS: list[tuple[str, tuple[str, ...]]] = [
    ("location", ("живёт", "живет", "город", "переех", "из москв", "из питер",
                  "из спб", "родом", "проживает", "обитает", "находится в")),
    ("age", ("лет", "возраст", "родился", "года рождения", "мне ", "ему ",
             "ей ", "стукнуло", "исполнил")),
    ("job", ("работа", "работает", "профес", "должност", "зарабатыва",
             "бизнес", "фрилан", "безработ", "уволил", "устроил")),
    ("study", ("учится", "учеба", "учёба", "универ", "школ", "студент",
               "課", "колледж", "вуз", "курс")),
    ("relationship", ("девушк", "парень", "жена", "муж", "встречается",
                      "развёл", "развел", "женил", "расстал", "влюбл",
                      "холост", "в отношениях", "семья")),
    ("pet", ("кот", "пёс", "пес", "собак", "питомец", "хомяк", "кошк")),
    ("car", ("машина", "тачка", "авто", "права", "водит", "купил машину")),
    ("health", ("болен", "болеет", "здоров", "лежит в больниц", "врач",
                "диагноз", "лечит")),
]


def detect_topic(fact: str) -> str | None:
    """Тема факта (location/age/job/...) или None, если не распознана."""
    low = (fact or "").lower()
    for topic, markers in _TOPIC_MARKERS:
        if any(m in low for m in markers):
            return topic
    return None


def _norm_fact(fact: str) -> str:
    """Нормализация для дедупа: схлопываем пробелы, нижний регистр, без хвостов."""
    return re.sub(r"\s+", " ", (fact or "").strip().lower()).rstrip(".!? ")


def merge_self_facts(
    prev: list[str], new: list[str], *, cap: int = 12
) -> list[str]:
    """Сливает факты о человеке так, чтобы свежие ВЫТЕСНЯЛИ устаревшие по теме.

    Правила:
    * порядок = хронология (старое слева, свежее справа); ``new`` считается
      более актуальным, чем ``prev``;
    * если свежий факт имеет ту же ТЕМУ, что и более старый — старый удаляется
      (человек переехал/сменил работу — помним последнее);
    * точные дубликаты схлопываются;
    * факты без распознанной темы просто копятся (с дедупом);
    * на выходе не больше ``cap`` фактов (отрезаем самые старые).
    """
    result: list[str] = []
    seen_norm: set[str] = set()
    # Идём от старых к свежим; свежие затирают старые по теме.
    ordered = list(prev) + list(new)
    # Тема → индекс в result (последний факт этой темы).
    topic_at: dict[str, int] = {}
    for fact in ordered:
        f = (fact or "").strip()
        if not f:
            continue
        norm = _norm_fact(f)
        if norm in seen_norm:
            # точный повтор — обновим позицию (факт «подтверждён» свежим)
            continue
        topic = detect_topic(f)
        if topic and topic in topic_at:
            # Свежий факт по той же теме вытесняет прежний.
            old_idx = topic_at[topic]
            old_norm = _norm_fact(result[old_idx])
            seen_norm.discard(old_norm)
            result[old_idx] = ""  # помечаем на удаление
        seen_norm.add(norm)
        result.append(f)
        if topic:
            topic_at[topic] = len(result) - 1
    cleaned = [f for f in result if f]
    return cleaned[-cap:]
