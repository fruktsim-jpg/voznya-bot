"""Пост-фильтр вывода друна: режет «корпоративные» фразы и нормализует длину.

Список запрещённого взят из ``ПЕРСОНАЖ.txt`` («Что Возня НЕ говорит»). Если
модель всё же выдала официальную формулировку — мягко вычищаем/обрезаем, чтобы
друн не звучал как сервис. Это последний рубеж; основная работа — в промпте.
"""

from __future__ import annotations

import re

# Фразы-маркеры официоза (нижний регистр, подстрока).
BANNED_SNIPPETS = (
    "уважаемый пользователь",
    "добро пожаловать",
    "операция выполнена",
    "ошибка выполнения",
    "авторизация завершена",
    "пользователь найден",
    "как ии",
    "как языковая модель",
    "как искусственный интеллект",
    "я не могу",
    "извините",
)


def has_banned(text: str) -> bool:
    """Есть ли в тексте запрещённые официальные формулировки."""
    low = text.lower()
    return any(snippet in low for snippet in BANNED_SNIPPETS)


def clean(text: str, *, max_chars: int = 600) -> str:
    """Чистит вывод: убирает обёртки кавычек/код-блоков, режет длину."""
    out = text.strip()
    # Снять обрамляющие markdown-код-блоки/кавычки, если модель обернула ответ.
    out = re.sub(r"^```[a-zA-Z]*\n?|```$", "", out).strip()
    if len(out) >= 2 and out[0] in {'"', "«", "'"} and out[-1] in {'"', "»", "'"}:
        out = out[1:-1].strip()
    if len(out) > max_chars:
        out = out[: max_chars - 1].rstrip() + "…"
    return out
