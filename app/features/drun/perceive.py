"""Восприятие и решение о вовлечении — «захотел ли друн ввязаться сам».

Это мозг агентности на РЕАКТИВНОЙ стороне. Раньше единственный путь, по которому
друн встревал без прямого обращения, — слепой бросок монетки
(``random_butt_in_chance``). Это Q&A-логика: «иногда ляпни что-нибудь». Живой
участник чата ведёт себя иначе — он РЕАГИРУЕТ на сигналы: кого-то опускают,
кто-то тильтует, задан вопрос в воздух, всплыла его тема, кто-то хвастается.

``decide_engagement`` — чистая, дешёвая функция (без БД и LLM): по тексту реплики
и обстановке выбирает НАМЕРЕНИЕ (intent) и силу позыва (urge 0..1). Решение
«говорить/молчать» принимает вызывающий, сверяясь с governor/кулдауном/капом.
LLM зовётся ТОЛЬКО когда уже решено говорить — намерение направляет генерацию.

Чистота функции = тестируемость и нулевая стоимость на горячем пути.
"""

from __future__ import annotations

import re
from dataclasses import dataclass
from enum import Enum


class Intent(str, Enum):
    """Зачем друн встревает (направляет тон генерации)."""

    SILENT = "silent"      # промолчать
    ROAST = "roast"        # подколоть/добить (кого-то опускают, фейл, хвастовство)
    SUPPORT = "support"    # поддержать/заступиться (кого-то травят, расстроен)
    STIR = "stir"          # подлить движа (вялый спор, скука)
    COMMENT = "comment"    # просто живая реплика в тему
    HYPE = "hype"          # раскачать (победа, крупное событие, кураж)


@dataclass(frozen=True)
class Engagement:
    """Решение восприятия: намерение + сила позыва + причина (для промпта/лога)."""

    intent: Intent
    urge: float          # 0..1 — насколько сильно тянет вставить слово
    reason: str          # короткое объяснение (идёт в задание генерации)

    @property
    def wants_in(self) -> bool:
        return self.intent is not Intent.SILENT and self.urge > 0.0


# --- Лексические сигналы (рус., нижний регистр; грубо, но дёшево и предсказуемо).
_AGGRO = (
    "лох", "лошара", "нищеброд", "даун", "тупой", "тупица", "дебил", "клоун",
    "слабак", "сосёшь", "сосет", "проиграл", "слил", "обосрал", "позор",
    "унижен", "ничтожество", "пусто", "банкрот", "нищий",
)
_DISTRESS = (
    "грустно", "плохо", "хреново", "устал", "достало", "бесит", "заебало",
    "не везёт", "не везет", "обидно", "больно", "одиноко", "тяжело", "депрессия",
)
_BRAG = (
    "изи", "легко", "ливанул", "затащил", "имба", "лучший", "я król", "я круче",
    "разнёс", "разнес", "вынес", "апнул", "поднял", "выиграл", "джекпот",
    "топ 1", "топ1", "нагнул",
)
_BOREDOM = (
    "скучно", "тишина", "никто не пишет", "тут есть кто", "оживите", "движа нет",
    "мёртвый чат", "мертвый чат", "тухло",
)
_OPEN_QUESTION = (
    "как думаете", "кто-нибудь", "кто нибудь", "есть кто", "что делать",
    "посоветуйте", "что лучше", "кто за", "кто против", "согласны",
)


def _hit(text: str, needles: tuple[str, ...]) -> bool:
    return any(n in text for n in needles)


def decide_engagement(
    text: str,
    *,
    chat_hot: int,
    mentions_drun_topic: bool = False,
    addressed_other: bool = False,
) -> Engagement:
    """Выбирает намерение и силу позыва по реплике и обстановке. Чистая функция.

    :param text: текст реплики игрока (уже без команд/мусора).
    :param chat_hot: сколько живых реплик за последние ~3 минуты (накал).
    :param mentions_drun_topic: всплыла тема, на которую у друна есть мнение.
    :param addressed_other: реплика явно адресована другому человеку (реплай).
    """
    low = (text or "").lower().strip()
    if not low:
        return Engagement(Intent.SILENT, 0.0, "")

    # Кого-то опускают/идёт наезд → друн любит добить или, наоборот, заступиться.
    # По умолчанию подкалывает (он дерзкий), но если жертва в дистрессе — может
    # и поддержать. Это решает тон, а не факт встревания.
    if _hit(low, _AGGRO):
        return Engagement(
            Intent.ROAST, 0.7,
            "в чате идёт наезд/опускание — влезь дерзко, добей шуткой или "
            "переверни на нападающего, ты тут главный по подъёбам",
        )

    # Хвастовство/кураж → подколоть выпендрёж или раскачать.
    if _hit(low, _BRAG):
        return Engagement(
            Intent.ROAST, 0.55,
            "кто-то хвастается/куражится — сбей спесь метким подъёбом или "
            "поддень по-доброму, не дай задрать нос",
        )

    # Расстроенный игрок → редкая искренняя (но в образе) поддержка/подъёб.
    if _hit(low, _DISTRESS):
        return Engagement(
            Intent.SUPPORT, 0.45,
            "человек скис/жалуется — кинь грубоватую поддержку в своём стиле "
            "(подбодри через подъёб), без сюсюканья",
        )

    # Скука/жалоба на тишину → создать движ.
    if _hit(low, _BOREDOM):
        return Engagement(
            Intent.STIR, 0.6,
            "жалуются на скуку/тишину — подлей движа: дерзкий вопрос, вызов "
            "или провокация, чтобы чат ожил",
        )

    # Открытый вопрос в воздух → влезь с мнением (но не если адресовано другому).
    if not addressed_other and _hit(low, _OPEN_QUESTION):
        return Engagement(
            Intent.COMMENT, 0.5,
            "задан открытый вопрос в чат — вставь своё дерзкое мнение коротко",
        )

    # Всплыла «его» тема (упомянут друн/ешки/казино/дуэли в третьем лице).
    if mentions_drun_topic and not addressed_other:
        return Engagement(
            Intent.COMMENT, 0.4,
            "зашла тема, по которой у тебя есть позиция — вставь реплику в тему",
        )

    # Иначе — почти всегда молчим. Лёгкий позыв вставить слово оставляем только
    # когда чат и так кипит (есть к чему прицепиться), и то слабый — финальное
    # решение и рандом за вызывающим.
    if chat_hot >= 6:
        return Engagement(
            Intent.COMMENT, 0.15,
            "чат оживлён — можешь тонко вкинуться в тему одной репликой",
        )
    return Engagement(Intent.SILENT, 0.0, "")


# Тема, на которую у друна всегда есть мнение (упоминание в третьем лице).
_DRUN_TOPIC = re.compile(
    r"\b(друн|drun|ешк|казино|дуэл|клад|ферм|ммр|mmr|рейтинг|пидор)\w*",
    re.IGNORECASE,
)


def mentions_drun_topic(text: str) -> bool:
    """Всплыла ли в реплике «его» тема (для COMMENT-намерения)."""
    return bool(_DRUN_TOPIC.search(text or ""))
